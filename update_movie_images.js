async function checkAndUpdateAllMovies() {
  try {
    await mongoose.connect(process.env.CONNECTION_URI);
    console.log('Connected to MongoDB');
    
    // Get all movies in your database
    const movies = await Models.Movie.find({}, 'Title ImagePath');
    console.log(`Found ${movies.length} movies in database:`);
    
    // Update each movie with a working image
    for (let i = 0; i < movies.length; i++) {
      const movie = movies[i];
      const newImageUrl = workingImages[i % workingImages.length]; // Cycle through images if more movies than images
      
      await Models.Movie.updateOne(
        { _id: movie._id },
        { $set: { ImagePath: newImageUrl } }
      );
      
      console.log(`✅ Updated "${movie.Title}" with working image`);
    }
    
    console.log('\n🎬 All movies updated with working images!');
    console.log('Restart your frontend to see the results.');
    
    process.exit(0);
  } catch (error) {
    console.error('❌ Error:', error);
    process.exit(1);
  }
}

checkAndUpdateAllMovies();